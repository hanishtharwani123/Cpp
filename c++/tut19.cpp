// SWAPING OF TWO NUMBER
#include <iostream>
using namespace std;
int main() {

    // SWAPPING
    int num1, num2, c;
    cout << "Enter the num1: ";
    cin >> num1;

    cout << "Enter the num2: ";
    cin >> num2;

    c = num1;
    num1 = num2;
    num2 = c;

    cout << "SWAPPING OF TWO NUMBER" << endl;
    cout << "num1 " << num1 << endl;
    cout << "num2 " << num2 << endl;


}