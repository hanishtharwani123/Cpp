// WORKING WITH NUMBERS 
# include <iostream>
# include <cmath>
using namespace std;
int main(){

  /*ARITHMETIC OPERATOR
    int num1 = 45;
    int num2 = 55;
    cout << num1 + num2 << endl;
    cout << num1 - num2 << endl;
    cout << num1 * num2 << endl;
    cout << num1 / num2 << endl;
    cout << num1 % num2 << endl;
  */

  /*INCREAMENT AND DECREAMENT OPERATOR
    int number = 999;
    number++;
    number--;
    cout << number << endl;
  */

  /*ASSINGMENT AND AUGUMENTED OPERATOR
    int value = 1500;
    value += 500;
    value -= 500;
    value *= 5;
    value /= 5;
    cout << value << endl;
  */

  /*ORDER OF OPERATION
    cout << 2 + 5 *10 << endl;
    cout << (2 + 5) * 10 << endl;
    cout << 2 * 5 / 5 << endl;
    cout << (2 * 5) / 5 << endl;
  */
    
  /*IMPORTANT MATH OPERATOR
    cout << pow(2, 10) << endl;
    cout << sqrt(456) << endl;
    cout << round(45.6) << endl;
    cout << ceil(43.3) << endl;
    cout << floor(43.3) << endl;
    cout << fmax(24, 4) << endl;
    cout << fmin(23, 4) << endl; 
  */
    return 0;

}
