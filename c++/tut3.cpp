// DATA TYPES
#include <iostream>
using namespace std;
int main(){
    
    /* DATA TYPES ARE TWO TYPES
       1. PRIMITIVE DATA TYPES (int , bool, float, char)
       2. NON PRIMITIVE DATA TYPES (strings, text message like complex object)
    */

    // PRIMITIVE DATA TYPE
    int age = 12;
    float dec = 12.5;
    double deci = 45.5;
    char word = 'A';
    bool name = true;

    cout << age << endl;
    cout << dec << endl;
    cout << deci << endl;
    cout << word << endl;
    cout << name << endl;


    // NON PRIMITIVE DATA TYPE
    string story = "Hello ji how are you i am hanish singh khan kese ho aap sab";
    string statem = "Once upon time in mumbai";

    cout << story << endl;
    cout << statem << endl;
    return 0;
}

